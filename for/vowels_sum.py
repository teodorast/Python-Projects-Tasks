some_text=input()
total_vowels = 0
for character in some_text:
    if character =="a":
        total_vowels = total_vowels+1
    elif character =="e":
        total_vowels = total_vowels+2
    elif character =="i":
        total_vowels = total_vowels+3
    elif character =="o":
        total_vowels = total_vowels+4
    elif character =="u":
        total_vowels = total_vowels+5
print(total_vowels)

    
